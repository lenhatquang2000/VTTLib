        <p class="text-gray-700 leading-relaxed mt-3"><strong>3.</strong> Bạn đọc tự quản lý tài sản, các dụng cụ cá nhân khi sử dụng Thư viện Trường.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>4.</strong> Không mang đồ ăn thức uống vào Thư viện Trường (ngoại trừ nước đóng chai).</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>5.</strong> Không viết, vẽ, gác chân lên ghế, bàn, tường.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>6.</strong> Bạn đọc là cán bộ, giảng viên, nhân viên và sinh viên Trường Đại học Võ Trường Toản thực hiện đồng phục Trường theo quy định vào các ngày thứ Hai, Tư, Sáu hàng tuần. Các ngày còn lại ăn mặc lịch sự gọn gàng, đeo thẻ. Thư viện từ chối phục vụ đối với những bạn đọc trang phục không phù hợp.</p>
    </div>
