        <p class="text-gray-700 leading-relaxed"><strong>2.</strong> Bạn đọc phải xuất trình thẻ khi vào Thư viện Trường, mượn tài liệu, đăng ký sử dụng máy tính hoặc khi cán bộ thư viện (CBTV) yêu cầu.</p>
        <p class="text-gray-700 leading-relaxed"><strong>3.</strong> Bạn đọc không được cho mượn hoặc sử dụng thẻ thư viện của người khác.</p>
        <p class="text-gray-700 leading-relaxed"><strong>4.</strong> Khi mất thẻ thư viện phải báo ngay với CBTV để khóa tài khoản và làm thủ tục cấp lại tài khoản sử dụng Thư viện theo quy định của Nhà trường.</p>
