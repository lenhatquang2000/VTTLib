    <h2 class="text-2xl font-bold text-gray-900">Chương II: KHAI THÁC VÀ SỬ DỤNG TÀI LIỆU</h2>

    <div>
        <h3 class="text-lg font-bold text-gray-900 mt-4 mb-2">Điều 4: Quy định sử dụng tài liệu in</h3>
        <p class="text-gray-700 leading-relaxed"><strong>1.</strong> Đối với tài liệu đọc tại chỗ, bạn đọc làm thủ tục mượn và trả tài liệu tại Quầy lưu hành. Trong thời gian sử dụng tài liệu, nếu cần ra khỏi Thư viện Trường, bạn đọc để tài liệu lại tại "Kệ để sách, báo sau khi đọc".</p>
        <ul class="text-gray-700 leading-relaxed ml-4 space-y-1 mt-2">
            <li>- Cán bộ, giảng viên, nhân viên được mượn 05 tài liệu/lượt.</li>
            <li>- Bạn đọc khác được mượn 03 tài liệu/lượt.</li>
        </ul>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>2.</strong> Đối với tài liệu mượn về, bạn đọc làm thủ tục mượn và trả tài liệu tại Quầy lưu hành. Bạn đọc phải trả sách đúng quy định, đúng về số lượng và thời hạn mượn:</p>
        <ul class="text-gray-700 leading-relaxed ml-4 space-y-1 mt-2">
            <li>- Cán bộ, giảng viên, nhân viên được mượn 05 tài liệu/2 tuần (Được gia hạn 01 lần, thời gian gia hạn thêm 02 tuần, tính từ ngày thực hiện lệnh gia hạn).</li>
            <li>- Bạn đọc khác được mượn: 03 tài liệu/tuần (Được gia hạn 01 lần, thời gian gia hạn thêm 01 tuần, tính từ ngày thực hiện lệnh gia hạn).</li>
            <li>- Đến hạn trả tài liệu, nếu muốn sử dụng tiếp bạn đọc cần gia hạn tài liệu. Tài liệu trả quá hạn bị phạt hành chính theo quy định.</li>
        </ul>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>3.</strong> Bạn đọc cần kiểm tra trạng thái tài liệu trước khi mượn. Nếu phát hiện sách, báo mất trang, rách, cắt xén, gạch xóa, bạn đọc phải báo ngay cho CBTV, nếu không mọi hư hỏng sẽ do bạn đọc hoàn toàn chịu trách nhiệm.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>4.</strong> Bạn đọc không tự ý vẽ, viết, gạch xóa, cắt xén, làm rách sách, báo.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>5.</strong> Bạn đọc không tự động thay đổi mã vạch, loại bỏ bi đỏ, phân loại số DDC của sách.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>6.</strong> Chấp hành các quy định tại Luật sở hữu trí tuệ và các quy định của pháp luật khác có liên quan, không sao chụp tài liệu của Thư viện Trường khi chưa được phép.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>7.</strong> Thư viện Trường phục vụ bạn đọc theo hình thức kho đóng, bạn đọc có thể sử dụng mạng, máy tính tại Thư viện Trường để tra cứu tài liệu và liên hệ CBTV để mượn tài liệu trong ấn bản, kể cả mượn về nhà và mượn đọc tại chỗ.</p>
    </div>

    <div>
        <h3 class="text-lg font-bold text-gray-900 mt-4 mb-2">Điều 5: Quy định sử dụng tài liệu điện tử</h3>
        <p class="text-gray-700 leading-relaxed">Tài liệu điện tử của Thư viện Trường bao gồm các cơ sở dữ liệu được Nhà trường mua quyền truy cập, nguồn tài liệu số của các thư viện hợp tác và nguồn tài liệu số của Thư viện Trường. Bạn đọc sử dụng tài liệu điện tử cần tuân thủ nghiêm túc các quy định sau:</p>
        <ul class="text-gray-700 leading-relaxed ml-4 space-y-2 mt-2">
            <li><strong>1.</strong> Chỉ sử dụng tài liệu cho mục đích học tập và nghiên cứu.</li>
            <li><strong>2.</strong> Phải tuyệt đối giữ bí mật tài khoản truy cập và mật khẩu truy cập đã được cấp; không được tiết lộ, chuyển giao cho người khác sử dụng.</li>
            <li><strong>3.</strong> Khai thác tài liệu điện tử phải tuân thủ quy định của Luật Sở hữu trí tuệ và các quy định của pháp luật khác có liên quan.</li>
            <li><strong>4.</strong> Không dùng các công cụ tin học để tải dữ liệu một cách có hệ thống và liên tục trong lượt truy cập với bất kỳ mục đích nào.</li>
            <li><strong>5.</strong> Nghiêm cấm hành vi sao chép, truyền, trao đổi, mua bán tài liệu điện tử.</li>
            <li><strong>6.</strong> Thông báo ngay cho Thư viện Trường hỗ trợ khi tài khoản truy cập không còn an toàn.</li>
        </ul>
    </div>

    <div>
        <h3 class="text-lg font-bold text-gray-900 mt-4 mb-2">Điều 6: Quy định sử dụng thiết bị và cơ sở vật chất</h3>
        <p class="text-gray-700 leading-relaxed"><strong>1.</strong> Phải đảm bảo quy định sử dụng thiết bị trong Thư viện Trường; không tự ý sử dụng và điều chỉnh thiết bị trong Thư viện Trường: Đèn, cửa sổ, quạt,… khi có sự cố về các thiết bị, bạn đọc phải báo ngay cho CBTV.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>2.</strong> Chỉ sử dụng máy tính cho mục đích học tập và nghiên cứu. Không dùng máy tính để chơi game, tải nhạc, tải phim và các loại hình giải trí khác. Nghiêm cấm sử dụng máy tính truy cập vào các trang web mang tính chất phản động và đồi trụy.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>3.</strong> Không tự ý di dời, tháo lắp, cài đặt phần mềm hoặc thay đổi thiết bị máy tính. Bạn đọc phải chịu trách nhiệm bồi thường nếu làm hỏng thiết bị.</p>
        <p class="text-gray-700 leading-relaxed mt-3"><strong>4.</strong> Không được làm hư hỏng, lấy cắp tài sản, trang thiết bị (sách, báo, vật dụng,…) trong Thư viện Trường.</p>
    </div>

